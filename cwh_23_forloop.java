public class cwh_23_forloop {
    public static void main(String[] args) {
        // odd number by increamenting
 //       int n = 10;
      for(int i=1;i<=10;i++){
            System.out.println(2*i+1);
        }
        // decreament 
        for(int i=5;i>0;i--){
            System.out.println(i);
        }
    }
    
}
